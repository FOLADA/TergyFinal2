const CitatebiBox: React.FC<ITitle> = ({ title, paragraph }) => {
    const [isOpen, setIsOpen] = useState(false);
    const [isNoteOpen, setIsNoteOpen] = useState(false);
    const [note, setNote] = useState<string>('');

    const toggleDivs = () => {
        setIsOpen(!isOpen);
    };

    const toggleNoteSection = () => {
        setIsNoteOpen(!isNoteOpen);
    };

    const handleSaveNote = () => {
        localStorage.setItem(`note-${title}`, note);
        setIsNoteOpen(false);
    };

    return (
        <div className="periphrazed-box">
            <div className="periphrazed-box-header">
                <h3 className="periphrazed-box-title">
                    <strong>{title}</strong>
                </h3>
                <button className="periphrazed-box-button" onClick={toggleDivs}>
                    {isOpen ? 'დახურე' : 'გაიაზრე'}
                </button>
            </div>
            {isOpen && (
                <div className="periphrazed-expanded-container">
                    <div className="periphrazed-expanded-content">
                        {paragraph.map((chunk, index) => (
                            <p key={index}>{chunk}</p>
                        ))}
                    </div>

                    <div className="periphrazed-box-note">
                        <button className="periphrazed-box-button" onClick={toggleNoteSection}>
                            {isNoteOpen ? 'დახურე' : 'დამატე ჩანაწერი'}
                        </button>
                    </div>

                    {isNoteOpen && (
                        <div className="note-container">
                            <textarea
                                value={note}
                                onChange={(e) => setNote(e.target.value)}
                                placeholder="შეიყვანეთ თქვენი შენიშვნა..."
                            />
                            <button onClick={handleSaveNote}>შენახვა</button>
                        </div>
                    )}
                </div>
            )}
        </div>
    );
};

const Citatebi: React.FC = () => {
    return (
        <>
            <div className="periphrazed-title-container">
                <h1 className="periphrazed-title">აფორიზმები</h1>
            </div>
            {chapters.map((chapter, index) => (
                <CitatebiBox key={index} title={chapter.title} paragraph={chapter.paragraph} />
            ))}
        </>
    );
};

export default Citatebi;
